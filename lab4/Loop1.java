public class Loop1 {
   public static void main(String args[]) {
      
      for (int i = 1; i < 4; i++) {
      
         for (int k = 1; k < 3; k++) {
         
            System.out.print((i * k) + " ");
            
         } // end k loop
         
         System.out.println();
      } // end i loop
      
   } // end main
} // end class